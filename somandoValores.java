public class somandoValores{
    public static void main(String[]args){
        String titulo = "somando dois valores" ;
        int n1 = 3;   //devemos sempre dizer o tipo de dado da variável antes de declarar seu nome.
        int n2 = 4;
        System.out.println(titulo);
        System.out.println(n1 + n2); //pedimos para imprimir na tela a soma das duas variáveis.
}
}
